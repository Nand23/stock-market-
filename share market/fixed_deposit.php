    <h2 >What is Fixed Deposit ?</h2>
   <p class="eqfont">
   A fixed deposit, also known as an FD, is an investment instrument offered by banks, as well as non-banking 
   financial companies (NBFC) to their customers to help them save money. With an FD account, you can invest a 
   sizeable amount of money at a predetermined rate of interest for a fixed period. At the end of the tenure, 
   you receive the lump sum, along with an interest, which is a good money-saving plan. Banks offers different 
   rates of interest for a fixed deposit account.
    <br><br>
    You can choose a fixed deposit for a period ranging from minimum 7-14 days to maximum 10 years. 
    This is why an FD is sometimes called a term deposit. When you open a fixed deposit account at a 
    specific interest rate, it is guaranteed, for the rate of interest remains the same, irrespective 
    of any changes, which happen due to market fluctuations.
    <br><br>
    The interest you earn is either paid at maturity or on periodic basis depending on your choice. 
    You are not allowed to withdraw the money before the maturity. If you want to, you have to pay a penalty.
</p>
