    <h2>Different Types of Taxation </h2>
    <p class="eqfont2"><b>Income tax:</b> Governments impose income taxes on financial income 
        generated by all entities within their jurisdiction, including individuals and businesses.
        <br><br>
        <b>Corporate tax:</b> This type of tax is imposed on the profit of a business.
        <br><br>
        <b>Capital gains:</b> A tax on capital gains is imposed on any capital gains or 
        profits made by people or businesses from the sale of certain assets including stocks, bonds, or real estate.
        <br><br>
        <b>Property tax:</b> A property tax is asses by a local government and paid for by the owner of a property. 
        This tax is calculated based on property and land values. 
        <br><br>
        <b>Inheritance:</b> A type of tax levied on individuals who inherit the estate of a deceased person. 
        <br><br>
        <b>Sales tax:</b> A consumption tax imposed by a government on the sale of goods and services. 
            This can take the form of a value-added tax (VAT), a goods and services tax (GST), a state or provincial sales tax, or an excise tax. 
    </p>
  
