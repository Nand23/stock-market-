    <h2 >What is Financial Planning ?</h2>
   <p class="eqfont">
   Financial planning is the practice of putting together a plan for your future, specifically
    around how you will manage your finances and prepare for all of the potential costs and issues 
    that may arise. The process involves evaluating your current financial situation, identifying your 
    goals and then developing and implementing relevant recommendations.</p>

        <h2>Different Types of Financial Planning : </h2>
        <p class="eqfont">
        A financial planner may offer a variety of services to you. These services will often be considered in 
        concert with one another. This helps the planner put together an overall plan that considers all aspects 
        of your current situation and future aspirations.
        <br><br>
        Here are eight common services that are generally offered as part of financial planning:
            <br><br><br><br><br><br>
</p>

           <h2>1. Tax planning</h2><br>
           <p class="eqfont2">
           Financial planners often help clients address certain tax issues. They can also figure out how 
           to maximize your tax refunds and minimize your tax liability. Certain advisors may also be able 
           to actually help you with preparing your taxes and filing your annual taxes.<br><br>

           </p>

<div class="eqcenten2">
        <img src="./image/services/finacial/f5.jpg" ali=""  />
        </div>
           <h2>2. Estate planning</h2>
          <p class="eqfont2"> Estate planning seeks to make things a bit easier for your loved ones after 
              you die. Preparing a will may be part of a financial planner’s services. Estate planning also 
              helps prepare for any estate tax you may be subject to.
            </p>
        
<br>
        

        <h2>3. Retirement planning</h2>
          <p class="eqfont2"> You presumably want to stop working some day. Retirement planning services help you
              prepare for that day. They ensure that you’ve saved enough money to live the lifestyle you want in 
              retirement. <br><br>
              

              <h2>4. Philanthropic planning</h2>
          <p class="eqfont2">  It’s always nice to give something to people who need it or help a cause close 
              to your heart. Financial planning can help you ensure you’re doing it efficiently and getting all
               the tax benefits you’re eligible for.<br><br>

               <h2>4. Education funding planning</h2>
          <p class="eqfont2"> If you have children or other dependents who wish to pursue a college degree, 
            you may want to help them to pay for it. Financial planning can help make sure you are able to do so.<br><br>
             
            <div class="eqcenten">
        <img src="./image/services/finacial/f6.jpg" ali=""  />
        </div>

            <h2>5. Investment planning</h2>
          <p class="eqfont">Though financial planning doesn’t include the actual management of your assets, 
              it can still help with your investment portfolio by mapping out how much you should be investing
               and in which types of investments.<br><br>

               <h2>6. Insurance planning</h2>
          <p class="eqfont">A financial planner can help you evaluate your insurance needs. Some financial 
              planners are also licensed insurance agents and can sell you insurance themselves. However, 
              they’ll likely earn a commission, which would create a conflict of interest.<br><br>

               <h2>7. Budgeting</h2>
          <p class="eqfont">This is perhaps the cornerstone of financial planning. A planner can make sure 
              you are spending the right amount given your income and can also make sure that you aren’t going
               into debt.<br><br>
    </p>
