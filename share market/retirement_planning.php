   <p class="eqfont">
   Retirement planning is being prepared for life after paid working period ends financially as 
   well as all other aspects of life. The non-financial aspects include lifestyle choices such as 
   spending time during retirement, a place to live, designated time to completely quit working, and 
   others. A holistic retirement planning considers all the areas with equal importance.<br><br>

   The level of emphasis on retirement planning varies throughout different life stages. During the youth, 
   retirement planning only means setting aside enough funds for retirement. During the middle of the career, 
   it might change to setting specific income/asset targets and taking the necessary steps to realise them. 
   Once you reach retirement, decades of savings will pay out.

