    <h2 >What is Insurance ?</h2>
   <p class="eqfont">
   An insurance policy/plan is an contact between an individual (Policyholder) and an insurance company (Provider). 
   Under the contract, you pay regular amounts of money (as premiums) to the insurer, and they pay you if the sum 
   assured on unfortunate event arises, for example, untimely demise of the life insured, an accident, or damage 
   to a house. Let’s know more about what is insurance and what are the various benefits, features & types of 
   insurance available in India.</p>

        <h2>Insurance Components : </h2>
        <p class="eqfont">
        Here are some of these components to help you better understand ‘what is insurance’ and how it works:
            <br><br><br><br><br><br><br>
</p>

           <h2>1. Insurance Premium Policy</h2><br>
           <p class="eqfont2">
           The premium of an insurance policy is the amount that you need to pay to purchase a specific amount 
           of insurance cover. It is typically expressed as a regular cost, be it monthly, quarterly, half-yearly, 
           or annually, that you incur during the premium payment term.<br><br>

           There are various factors based on which an insurance company calculates the premium of an insurance policy. 
           The idea behind is to check the eligibility of an insured individual for the specific type of insurance policy
            that he/she wants to buy.<br><br>

            For example, if you are healthy and do not have a medical history of getting treatment for severe 
            bodily diseases, you will likely to pay less for health insurance or life insurance policy than 
            someone suffering from multiple ailments. <br><br>

            You should also know that different insurance companies may ask for different premiums for 
            similar types of policies. So, selecting the right one at a price you can afford does require 
            some effort.<br><br>
</p>

