           <h2>2. Policy Limit</h2>
          <p class="eqfont2"> It is defined as the maximum amount that an insurance company is liable to pay 
              for the losses covered under the insurance policy. It is determined based on the period (policy term), 
              loss or injury, and similar other factors.<br><br>

              Typically, higher the policy limit, higher will be the premium payable. For a life insurance policy, 
              the maximum amount that an insurer pays to the nominee is known as the sum assured.
            </p>
        
<br>
        

        <h2>3. Deductible</h2>
          <p class="eqfont2"> Deductible related to an insurance policy is the amount or percentage that the 
              policyholder agrees to pay out of pocket before the insurer sets in to settle a claim. 
              You can also think of it as a deterrent to small, insignificant claims that many people file under 
              their insurance policies. <br><br>
              Deductibles are applicable per policy or per claim as defined by the terms of a specific type 
              of policy. Generally, insurance policies bought with high deductibles are less expensive as the
               higher out-of-pocket expense results in fewer claims.

