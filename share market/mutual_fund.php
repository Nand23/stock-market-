        <h2>3.Diversification</h2>
          <p class="eqfont2"> You may have heard the saying: Don’t put all your eggs in one basket. 
              This is a famous mantra to remember when you invest your money. When you invest only 
              in a single asset, you could risk a loss if the market crashes. However, you can avoid 
              this problem by investing in different asset classes and diversifying your portfolio. <br><br>
              If you were investing in stocks and had to diversify, you would have to select at least 
              ten stocks carefully from different sectors. This can be a lengthy, time-consuming process. 
              But when you invest in mutual funds, you achieve diversification instantly. For instance, 
              if you invest in a mutual fund that tracks the BSE Sensex, you would get access to as many 
              as 30 stocks across sectors in a single fund. This could reduce your risk to a large extent.

              <h2>3.Diversification</h2>
          <p class="eqfont2"> Mutual fund investors can claim a tax deduction of up to Rs. 1.5 lakh by investing 
              in Equity Linked Savings Schemes (ELSS). This tax benefit is eligible under Section 80C of the 
              Income Tax Act. ELSS funds come with a lock-in period of 3 years. Hence, if you invest in ELSS 
              funds, you can only withdraw your money after the lock-in period ends. <br><br>
              Another tax benefit is indexation benefit available on debt funds. In case of traditional 
              products, all interest earned is subject to tax. However, in case of debt mutual funds, 
              only the returns earned over and above the inflation rate (embedded in cost inflation index 
              {CII}) are subject to tax. This could also help investors earn higher post tax returns.
    </p>
        
