    <h2 >What is Mutual Fund ?</h2>
   <p class="eqfont">
   A mutual fund is an investment vehicle where many investors pool their money to earn returns on 
   their capital over a period. This corpus of funds is managed by an investment professional known 
   as a fund manager or portfolio manager. It is his/her job to invest the corpus in different securities 
   such as bonds, stocks, gold and other assets and seek to provide potential returns. The gains (or losses) 
   on the investment are shared collectively by the investors in proportion to their contribution to the fund.</p>

