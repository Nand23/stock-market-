           <h2>2.Returns</h2>
          <p class="eqfont2"> One of the biggest mutual fund benefits is that you have the opportunity to earn 
              potentially higher returns than traditional investment options offering assured returns. 
              This is because the returns on mutual funds are linked to the market’s performance. 
              So, if the market is on a bull run and it does exceedingly well, the impact would be reflected 
              in the value of your fund. However, a poor performance in the market could negatively impact your 
              investments. Unlike traditional investments ,mutual funds do not assure capital protection.
               So do your research and invest in funds that can help you meet your financial goals at the right 
               time in life.</p>
        
